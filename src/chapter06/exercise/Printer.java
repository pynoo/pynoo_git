package chapter06.exercise;

public class Printer {
	void y(int a) {
		System.out.println(a);
	}
	void y(boolean b) {
		System.out.println(b);
	}
	void y(double c) {
		System.out.println(c);
	}
	void y(String d) {
		System.out.println(d);
	}
	
	static void z(int a) {
		System.out.println(a);
	}
	static void z(boolean b) {
		System.out.println(b);
	}
	static void z(double c) {
		System.out.println(c);
	}
	static void z(String d) {
		System.out.println(d);
	}
}
