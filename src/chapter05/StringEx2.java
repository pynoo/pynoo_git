package chapter05;

public class StringEx2 {
	public static void main(String[] args) {
		// String을 다른 타입으로 변환
		String strVar1 = "9876";
		int intVar1 = Integer.parseInt(strVar1);
		System.out.println(intVar1);
		
		String strVar2 = "3.14";
		double doubleVar1 = Double.parseDouble(strVar2);
		System.out.println(doubleVar1);
	}
}
